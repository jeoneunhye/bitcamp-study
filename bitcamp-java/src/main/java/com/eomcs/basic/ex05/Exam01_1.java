// 산술 연산자 : +, -, *, /, %
package com.eomcs.basic.ex05;

public class Exam01_1 {
  public static void main(String[] args) {
    System.out.println(100 + 27); // 127
    System.out.println(100 - 27); // 73
    System.out.println(100 * 27); // 2700
    System.out.println(100 / 27); // 3
    System.out.println(100 % 27); // 나눈 나머지 19
  }
}